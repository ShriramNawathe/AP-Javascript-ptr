// primituve data types

// name string boolean -(true false) null undefined
let fname="John";
console.log("First Name: " + fname);//John
console.log(typeof fname);//string

let age=30;
console.log("Age: " + age);//30
console.log(typeof age);//number

let isMarried=false;
console.log("Is Married: " + isMarried);//false
console.log(typeof isMarried);//boolean

let x=null;
console.log("Value of x: " + x);//null
console.log(typeof x);//object

let y;
console.log("Value of y: " + y);//undefined
console.log(typeof y);//undefined
